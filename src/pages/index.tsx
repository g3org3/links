import { type NextPage } from 'next'
import Head from 'next/head'
import type { FormEventHandler } from 'react'
import { useRef } from 'react'

import { trpc } from '../utils/trpc'

const Home: NextPage = () => {
  const { data } = trpc.example.links.useQuery()
  const utils = trpc.useContext()
  const inputRef = useRef<HTMLInputElement>(null)
  const addLink = trpc.example.addLink.useMutation({
    onSuccess() {
      if (inputRef.current?.value) {
        inputRef.current.value = ''
      }
      utils.example.links.invalidate()
    },
    onError(err) {
      console.log(err)
    },
  })

  const onAddLink: FormEventHandler<HTMLFormElement> = (e) => {
    e.preventDefault()
    if (!inputRef.current?.value) return
    addLink.mutate({ url: inputRef.current.value })
  }

  return (
    <>
      <Head>
        <title>Create T3 App</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main className="flex min-h-screen flex-col gap-4 bg-slate-200 p-4">
        <form onSubmit={onAddLink}>
          <div className="flex">
            <input
              disabled={addLink.isLoading}
              ref={inputRef}
              className="w-full rounded-lg border-2 p-2"
              placeholder="https://google.com..."
            />
            <button type="submit" className="hidden bg-blue-200 p-2">
              add
            </button>
          </div>
        </form>
        <hr />
        <div className="flex flex-wrap items-start gap-2">
          {data?.map((link) => (
            <a
              key={link.url}
              className="flex w-full flex-col items-center rounded-lg border-2 bg-white p-2 shadow-sm transition-all hover:bg-green-100 md:max-w-sm"
              target="_blank"
              href={link.url}
              rel="noreferrer"
            >
              <img alt="image" className="h-36" src={link.image} />
              <div className="font-semibold text-slate-700 line-clamp-2">{link.desc}</div>
              <div className="font-mono text-xs text-gray-400">{link.url}</div>
            </a>
          ))}
        </div>
      </main>
    </>
  )
}

export default Home
